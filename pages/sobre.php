    <h3 style="font-size:30px;font-family: 'Roboto Condensed', sans-serif;" data-aos="fade-in">Apresentação</h3>

    <p style='color: #8F8F8F; font-size: 14px; text-align: justify '>O INSTITUTO SABER, foi criado em 2002, com base na
        legislação atual e demonstra o propósito de envolver professores pesquisadorese alunos em um processo de
        reflexão pedagógica voltado a novos caminhos de qualificação profissional, caminhos estes que impõem engajamento
        social e político na construçaõ de uma sociedade mais humana, justa e solidária. Apresenta como missão
        "Produzir. desenvolver, aplicar e disseminar o saber, qualificar profissionais críticos, investigativos e
        formadores de opnião, capazes de identificar as principais questões de sua área, apontando soluções de forma
        eficaz e compromissar o profissional com a pesquisa, extensão e a responsabilidade Social".Insere-se na
        realidade do Distrito Federal e de seu entorno, bem como em vários estados brasileiros, comprometendo-se a
        contribuir para o desenvolvimento sócio-econômico-cultural, na medida em que promove a melhoria na capacitação
        de recursos humanos, em nível de pós-graduação lato sensu, e strictu sensu para atuarem numa perspectiva de
        qualidade e ética profissional, nas diversas áreas do conhecimento através de um programa de formação
        continuada. Além da proposta de formação continuada o Instituto Saber promove Congressos, Seminários, Simpósios
        e demais áreas do conhecimento. Com uma visão de vanguarda confirmou sua participação efetiva no mundo acadêmico
        como uma instituição de qualidade tornando-se referência em formação de educadores.</p>
    
